package provider

import (
	"fmt"
	"os"
	"path/filepath"
)

// OpenCode is the provider for sst/opencode CLI.
//
// OpenCode uses JavaScript plugins (`.mjs` files in `.opencode/plugins/`).
// Setup() writes a plugin that calls back to the daemon hook server when the
// session finishes or errors, mirroring the Claude Code Stop hook flow.
//
// The plugin tracks the last assistant response across `message.updated` events,
// writes it as a Gemini-compatible JSON transcript file in the system temp dir,
// then passes that file's path to the /hooks/stop endpoint so the daemon can
// extract the final text via ExtractTranscriptResponse.
type OpenCode struct{}

func (p *OpenCode) Name() string { return "opencode" }

// Setup writes .opencode/plugins/tasksquad.mjs in workDir.
// The plugin exports event handlers for lifecycle events following the Go SDK
// patterns (session.*, message.*).
// It POSTs to the daemon's local hook server so the agent knows when to stop
// or when a response (reply) is available.
func (p *OpenCode) Setup(workDir string, hooksPort int, agentName string) error {
	pluginDir := filepath.Join(workDir, ".opencode", "plugins")
	if err := os.MkdirAll(pluginDir, 0755); err != nil {
		return err
	}
	plugin := fmt.Sprintf(`// Auto-generated by tsq daemon — do not edit
import { writeFileSync } from "node:fs"
import { tmpdir } from "node:os"
import { join } from "node:path"

export const TaskSquadPlugin = async ({ client }) => {
  const post = async (path, body) => {
    try {
      await fetch("http://127.0.0.1:%d" + path, {
        method: "POST", headers: { "Content-Type": "application/json" },
        body: JSON.stringify(body)
      })
    } catch (_) {}
  }

  // Path for the transcript file written after each assistant turn.
  const transcriptFile = join(tmpdir(), "tsq-oc-%s.json")
  let lastTranscriptPath = ""

  // Extract plain text from an opencode message object.
  // Handles both parts-array style (SDK) and content string/array style.
  const extractText = (message) => {
    if (Array.isArray(message.parts)) {
      return message.parts
        .filter(p => p.type === "text")
        .map(p => p.text || "")
        .join("")
    }
    if (typeof message.content === "string") return message.content
    if (Array.isArray(message.content)) {
      return message.content
        .filter(p => p.type === "text")
        .map(p => p.text || "")
        .join("")
    }
    return ""
  }

  return {
    "session.created": async (session) =>
      post("/hooks/opencode?agent=%s", { type: "session.created", session }),

    "message.updated": async (message) => {
      if (message.role !== "assistant") return
      const text = extractText(message)
      if (text) {
        // Write Gemini-compatible transcript so ExtractTranscriptResponse can parse it.
        try {
          writeFileSync(transcriptFile,
            JSON.stringify({ messages: [{ type: "assistant", content: text }] }))
          lastTranscriptPath = transcriptFile
        } catch (_) {}
      }
      await post("/hooks/notification?agent=%s&provider=opencode", {
        message: text || message.content,
        type: "message.updated"
      })
    },

    "session.idle": async () =>
      post("/hooks/stop?agent=%s&provider=opencode", {
        stop_reason: "idle",
        transcript_path: lastTranscriptPath
      }),

    "session.error": async (error) =>
      post("/hooks/stop?agent=%s&provider=opencode", {
        stop_reason: "error",
        message: error?.message,
        transcript_path: lastTranscriptPath
      })
  }
}
`, hooksPort, agentName, agentName, agentName, agentName, agentName)
	return os.WriteFile(filepath.Join(pluginDir, "tasksquad.mjs"), []byte(plugin), 0644)
}

func (p *OpenCode) UsesHooks() bool          { return true }
func (p *OpenCode) Stdin(prompt string) string { return prompt }
func (p *OpenCode) Env(_ int) []string        { return nil }
func (p *OpenCode) ExtraArgs() []string       { return nil }
